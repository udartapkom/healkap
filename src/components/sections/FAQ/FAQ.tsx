export const FAQ: React.FC = () => {
  const [activeIndex, setActiveIndex] = useState<number | null>(null);

  const toggleItem = (index: number) => {
    setActiveIndex(activeIndex === index ? null : index);
  };

  return (
    <section className={styles.section}>
      <div className={styles.container}>
        <div className={styles.header}>
          <h2 className={styles.title}>Частые вопросы</h2>
          <p className={styles.description}>
            Если не нашли ответ — мы на связи 24/7
          </p>
        </div>
        
        <div className={styles.faqCard}>
          <div className={styles.faqItems}>
            {faqItems.map((item, index) => (
              <div
                key={index}
                className={styles.faqItem}
                onClick={() => toggleItem(index)}
              >
                <div className={styles.questionContainer}>
                  <h3 className={styles.question}>{item.question}</h3>
                  <div className={styles.icon}>
                    {activeIndex === index ? (
                      <img src="/images/minus-icon.svg" alt="Minus" width={20} height={20} />
                    ) : (
                      <img src="/images/plus-icon.svg" alt="Plus" width={20} height={20} />
                    )}
                  </div>
                </div>
                
                {activeIndex === index && (
                  <div className={styles.answer}>
                    <p>{item.answer}</p>
                  </div>
                )}
              </div>
            ))}
          </div>
          
          <div className={styles.fullListLink}>
            Перейти к полному списку вопросов
          </div>
        </div>
      </div>
    </section>
  );
};
